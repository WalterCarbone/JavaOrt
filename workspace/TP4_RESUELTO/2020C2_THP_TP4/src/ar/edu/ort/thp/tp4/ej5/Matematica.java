package ar.edu.ort.thp.tp4.ej5;

public class Matematica {

	/**
	 * Método estático que retorna el promedio a partir de una suma y una cantidad
	 * 
	 * @param sumaTotal
	 * @param cantidad
	 * @return Promedio (double)
	 */
	public static double obtenerPromedio(double sumaTotal, int cantidad) {
		return sumaTotal / cantidad;
	}

}
